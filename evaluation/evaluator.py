"""
Evaluation module for DocChat RAG system.

Metrics computed:
  • Precision@k  — fraction of top-k retrieved chunks that are "relevant"
                   (relevance = chunk text contains any expected keyword)
  • Recall@k     — fraction of expected keywords found across top-k chunks
  • Answer Score — LLM-as-judge scoring (1–5 scale) using GPT-4o-mini
  • Latency      — end-to-end response time in seconds

Usage:
    python -m evaluation.evaluator \\
        --test-set evaluation/test_set.json \\
        --output evaluation/results.csv \\
        --top-k 5 \\
        --strategy fixed

Results are written to CSV and a Markdown report is printed to stdout.
"""
from __future__ import annotations

import argparse
import json
import logging
import sys
import time
from pathlib import Path
from typing import Any

ROOT = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(ROOT))

import config
from embeddings.embedder import embed_query
from rag.prompt_templates import build_rag_prompt
from rag.retriever import retrieve
from vectorstore.chroma_store import ChromaStore

logger = logging.getLogger(__name__)

EvalResult = dict[str, Any]


# ─── Retrieval Metrics ───────────────────────────────────────────────────────

def precision_at_k(
    retrieved_chunks: list[dict],
    keywords: list[str],
    k: int,
) -> float:
    """
    Precision@k: fraction of the top-k retrieved chunks that are 'relevant'.
    A chunk is considered relevant if it contains at least one expected keyword.
    """
    if not retrieved_chunks or not keywords:
        return 0.0

    top_k = retrieved_chunks[:k]
    relevant_count = sum(
        1
        for chunk in top_k
        if any(kw.lower() in chunk["text"].lower() for kw in keywords)
    )
    return relevant_count / len(top_k)


def recall_at_k(
    retrieved_chunks: list[dict],
    keywords: list[str],
    k: int,
) -> float:
    """
    Recall@k: fraction of expected keywords found in any of the top-k chunks.
    This approximates how much of the relevant content was retrieved.
    """
    if not retrieved_chunks or not keywords:
        return 0.0

    top_k_text = " ".join(c["text"].lower() for c in retrieved_chunks[:k])
    found = sum(1 for kw in keywords if kw.lower() in top_k_text)
    return found / len(keywords)


# ─── LLM-as-Judge ────────────────────────────────────────────────────────────

_JUDGE_SYSTEM = """\
You are an expert evaluator assessing the quality of answers generated by a \
document Q&A system.

Score the provided answer on a scale of 1 to 5:
  5 - Fully correct, complete, and well-cited. Matches ground truth precisely.
  4 - Mostly correct with minor omissions or slight imprecision.
  3 - Partially correct. Key facts are present but answer is incomplete or vague.
  2 - Mostly incorrect or significantly incomplete.
  1 - Completely wrong, hallucinated, or refused to answer without reason.

Return ONLY a JSON object: {"score": <integer>, "reason": "<brief explanation>"}
"""


def llm_judge(
    question: str,
    generated_answer: str,
    ground_truth: str,
    api_key: str = "",
    model: str = "gpt-4o-mini",
) -> dict[str, Any]:
    """
    Use an LLM to score the generated answer against the ground truth.
    Returns {"score": int, "reason": str} or {"score": 0, "reason": "error msg"}.
    """
    try:
        from openai import OpenAI
        from rag.generator import resolve_provider
    except ImportError:
        return {"score": 0, "reason": "openai not installed"}

    chosen_provider, default_model, resolved_key, base_url = resolve_provider()
    
    # If the user didn't explicitly pass a model/key to the judge, use the active provider's defaults
    active_model = default_model if model == "gpt-4o-mini" else model
    active_key = api_key or resolved_key

    if chosen_provider == "ollama":
        active_key = active_key or "ollama"

    if not active_key:
        return {"score": 0, "reason": f"no API key for {chosen_provider}"}

    client_kwargs = {"api_key": active_key}
    if base_url:
        client_kwargs["base_url"] = base_url
        
    client = OpenAI(**client_kwargs)
    prompt = (
        f"Question: {question}\n\n"
        f"Ground Truth: {ground_truth}\n\n"
        f"Generated Answer: {generated_answer}\n\n"
        "Score the generated answer."
    )

    try:
        resp = client.chat.completions.create(
            model=active_model,
            messages=[
                {"role": "system", "content": _JUDGE_SYSTEM},
                {"role": "user", "content": prompt},
            ],
            temperature=0.0,
            max_tokens=256,
            response_format={"type": "json_object"},
        )
        raw = resp.choices[0].message.content or "{}"
        result = json.loads(raw)
        return {
            "score": int(result.get("score", 0)),
            "reason": str(result.get("reason", "")),
        }
    except Exception as exc:
        logger.warning(f"LLM judge failed: {exc}")
        return {"score": 0, "reason": str(exc)}


# ─── Full Evaluation Run ─────────────────────────────────────────────────────

def run_evaluation(
    test_set_path: str | Path,
    store: ChromaStore,
    top_k: int = 5,
    score_threshold: float = 0.1,
    use_llm_judge: bool = True,
    output_csv: str | Path | None = None,
) -> list[EvalResult]:
    """
    Run the full evaluation suite over all Q&A pairs in the test set.

    Args:
        test_set_path:  Path to test_set.json.
        store:          ChromaStore with indexed documents.
        top_k:          Number of chunks to retrieve per query.
        score_threshold: Minimum cosine score filter.
        use_llm_judge:  Whether to call the LLM judge for answer scoring.
        output_csv:     Optional path to write results CSV.

    Returns:
        List of per-question EvalResult dicts.
    """
    test_data = json.loads(Path(test_set_path).read_text(encoding="utf-8"))
    pairs = test_data.get("pairs", [])
    logger.info(f"Running evaluation on {len(pairs)} Q&A pairs (top_k={top_k})")

    results: list[EvalResult] = []

    for pair in pairs:
        qid = pair.get("id", "?")
        question = pair["question"]
        ground_truth = pair.get("ground_truth", "")
        keywords = pair.get("relevant_chunks_keywords", [])

        t0 = time.perf_counter()

        # Retrieve
        retrieved = retrieve(
            query=question,
            store=store,
            embed_fn=embed_query,
            top_k=top_k,
            score_threshold=score_threshold,
        )
        retrieval_time = time.perf_counter() - t0

        # Retrieval metrics
        p_at_k = precision_at_k(retrieved, keywords, top_k)
        r_at_k = recall_at_k(retrieved, keywords, top_k)
        top_score = retrieved[0]["score"] if retrieved else 0.0
        n_retrieved = len(retrieved)

        # Answer generation (optional — skipped if no API key)
        generated_answer = ""
        judge_score = None
        judge_reason = ""
        gen_time = 0.0

        has_api_key = any([
            getattr(config, "OPENAI_API_KEY", ""),
            getattr(config, "GROQ_API_KEY", ""),
            getattr(config, "GEMINI_API_KEY", ""),
        ])
        
        if has_api_key or getattr(config, "LLM_PROVIDER", "") == "ollama":
            max_retries = 3
            for attempt in range(max_retries):
                try:
                    from rag.generator import generate_answer
                    messages = build_rag_prompt(question, retrieved)
                    t1 = time.perf_counter()
                    gen_resp = generate_answer(messages, retrieved)
                    gen_time = time.perf_counter() - t1
                    generated_answer = gen_resp["answer"]

                    if use_llm_judge:
                        judge = llm_judge(question, generated_answer, ground_truth)
                        judge_score = judge.get("score")
                        judge_reason = judge.get("reason", "")
                    
                    break  # Success, exit the retry loop
                except Exception as exc:
                    err_msg = str(exc).lower()
                    if "rate limit" in err_msg or "429" in err_msg:
                        if attempt < max_retries - 1:
                            logger.warning(f"Rate limit hit for {qid}. Sleeping 20s and retrying... ({attempt+1}/{max_retries})")
                            time.sleep(20)
                            continue
                    
                    logger.warning(f"Generation failed for {qid}: {exc}")
                    generated_answer = f"[ERROR: {exc}]"
                    break

        result: EvalResult = {
            "id": qid,
            "category": pair.get("category", ""),
            "question": question,
            "ground_truth": ground_truth,
            "generated_answer": generated_answer,
            "n_retrieved": n_retrieved,
            "top_score": round(top_score, 4),
            "precision_at_k": round(p_at_k, 4),
            "recall_at_k": round(r_at_k, 4),
            "judge_score": judge_score,
            "judge_reason": judge_reason,
            "retrieval_time_s": round(retrieval_time, 3),
            "generation_time_s": round(gen_time, 3),
        }
        results.append(result)

        print(
            f"  [{qid}] P@{top_k}={p_at_k:.2f} | R@{top_k}={r_at_k:.2f} | "
            f"top_score={top_score:.3f} | judge={judge_score} | "
            f"{question[:60]}"
        )

        # Rate limit protection for free tiers (like Groq)
        time.sleep(4)

    # Write CSV
    if output_csv:
        _write_csv(results, Path(output_csv))

    _print_summary(results, top_k)
    return results


def _write_csv(results: list[EvalResult], path: Path) -> None:
    """Write results to a CSV file."""
    import csv

    path.parent.mkdir(parents=True, exist_ok=True)
    fieldnames = list(results[0].keys()) if results else []

    with path.open("w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(results)

    print(f"\n✅ Results saved to: {path}")


def _print_summary(results: list[EvalResult], k: int) -> None:
    """Print a summary table of aggregate metrics."""
    if not results:
        print("No results to summarize.")
        return

    n = len(results)
    avg_p = sum(r["precision_at_k"] for r in results) / n
    avg_r = sum(r["recall_at_k"] for r in results) / n
    avg_top = sum(r["top_score"] for r in results) / n
    avg_ret_t = sum(r["retrieval_time_s"] for r in results) / n

    judge_scores = [r["judge_score"] for r in results if r["judge_score"] is not None]
    avg_judge = sum(judge_scores) / len(judge_scores) if judge_scores else None

    print(f"\n{'═'*60}")
    print(f"  EVALUATION SUMMARY  ({n} questions, top_k={k})")
    print(f"{'═'*60}")
    print(f"  Avg Precision@{k}:       {avg_p:.3f}")
    print(f"  Avg Recall@{k}:          {avg_r:.3f}")
    print(f"  Avg Top Cosine Score:   {avg_top:.3f}")
    print(f"  Avg Retrieval Latency:  {avg_ret_t*1000:.1f} ms")
    if avg_judge is not None:
        print(f"  Avg LLM Judge Score:    {avg_judge:.2f} / 5.0 ({len(judge_scores)} scored)")
    print(f"{'═'*60}\n")


# ─── CLI ─────────────────────────────────────────────────────────────────────

if __name__ == "__main__":
    logging.basicConfig(level=logging.INFO)

    parser = argparse.ArgumentParser(description="DocChat Evaluation Script")
    parser.add_argument(
        "--test-set",
        default=str(ROOT / "evaluation" / "test_set.json"),
        help="Path to test_set.json",
    )
    parser.add_argument(
        "--output",
        default=str(ROOT / "evaluation" / "results.csv"),
        help="Output CSV path",
    )
    parser.add_argument("--top-k", type=int, default=5)
    parser.add_argument("--threshold", type=float, default=0.1)
    parser.add_argument("--no-judge", action="store_true", help="Skip LLM judge scoring")
    parser.add_argument(
        "--collection",
        default=config.COLLECTION_NAME,
        help="ChromaDB collection name",
    )
    args = parser.parse_args()

    store = ChromaStore(
        collection_name=args.collection,
        persist_dir=str(ROOT / "chroma_db"),
    )

    if store.count == 0:
        print("⚠️  Vector store is empty. Index documents first before running evaluation.")
        sys.exit(1)

    run_evaluation(
        test_set_path=args.test_set,
        store=store,
        top_k=args.top_k,
        score_threshold=args.threshold,
        use_llm_judge=not args.no_judge,
        output_csv=args.output,
    )
